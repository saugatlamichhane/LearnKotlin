fun main() {
    //var for variable
    var myVar = 7;
    //we can explicitly state the data type, but it is not required
    //var myVar: Int = 7
    println("myVar is $myVar")
    myVar = 4
    println("myVar is now $myVar")

    //val for constant
    val myVal = 1
    println("myVal is $myVal")

    //boolean
    val myBoolVar = true
    println("myBoolVar is $myBoolVar")

    //double
    val myDoubleVar = 8.23
    println("myDoubleVar is $myDoubleVar")

    //float
    val myFloatVar = 8.23F
    println("myFloatVar is $myFloatVar")

}
